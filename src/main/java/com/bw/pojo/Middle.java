package com.bw.pojo;

public class Middle {

	private Integer id;
	private Integer mid;
	private Integer did;
	public Middle() {
		super();
	}
	public Middle(Integer id, Integer mid, Integer did) {
		super();
		this.id = id;
		this.mid = mid;
		this.did = did;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getMid() {
		return mid;
	}
	public void setMid(Integer mid) {
		this.mid = mid;
	}
	public Integer getDid() {
		return did;
	}
	public void setDid(Integer did) {
		this.did = did;
	}
	
	
}
